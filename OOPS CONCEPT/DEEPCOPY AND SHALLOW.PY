'''
====================================================================
üìå PYTHON LIST ‚Äî SHALLOW COPY vs DEEP COPY (NOTES + EXAMPLES)
====================================================================

In Python, when we copy lists, they do NOT always create new memory.
Some copies only copy the reference, not the actual data.

To understand this, we study:
‚úî Shallow Copy  
‚úî Deep Copy  

WHY DO WE NEED THIS?
--------------------
‚úî To avoid accidental modification of original lists  
‚úî To properly duplicate nested lists  
‚úî To control memory usage  
‚úî To understand how Python stores mutable objects  

====================================================================
# 1Ô∏è‚É£ SHALLOW COPY
# ---------------------------------------------------------------
DEFINITION:
Shallow Copy creates a new list, but **inner elements still share
the same memory**.

Meaning:
‚úî Outer list is new  
‚úî Inner/nested lists are NOT copied  
‚úî If nested data is changed, both lists change  

WAYS TO DO SHALLOW COPY:
-------------------------
1. list.copy()
2. list slicing ‚Üí new_list = old_list[:]
3. copy.copy()

====================================================================
import copy

lst1 = [1, 2, [3, 4]]
lst2 = copy.copy(lst1)   # shallow copy

lst2[2][0] = 100   # modifying nested list

print(lst1)   # ‚Üí [1, 2, [100, 4]]  (changed)
print(lst2)   # ‚Üí [1, 2, [100, 4]]

====================================================================
# 2Ô∏è‚É£ DEEP COPY
# ---------------------------------------------------------------
DEFINITION:
Deep Copy creates a **completely independent** copy of both:
‚úî outer list  
‚úî all nested lists  

Meaning:
‚úî Changing nested values will NOT affect the original list.

HOW TO DO DEEP COPY:
--------------------
‚Üí Using: copy.deepcopy()
====================================================================
import copy

lst1 = [1, 2, [3, 4]]
lst2 = copy.deepcopy(lst1)

lst2[2][0] = 200

print(lst1)   # ‚Üí [1, 2, [3, 4]]  (unchanged)
print(lst2)   # ‚Üí [1, 2, [200, 4]]

'''