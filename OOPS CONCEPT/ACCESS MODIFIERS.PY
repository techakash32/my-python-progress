"""
====================================================================
üìå PYTHON ACCESS MODIFIERS (NOTES + EXAMPLES)
====================================================================

Access Modifiers are used to control the visibility of variables
and methods inside a class and outside the class.

# - WHY DO WE USE ACCESS MODIFIERS - ?
-------------------------------
‚úî To protect sensitive data
‚úî To prevent accidental modification  
‚úî To implement Encapsulation  
‚úî To organize code & maintain security  
‚úî To give controlled access using methods  

TYPES OF ACCESS MODIFIERS IN PYTHON:
------------------------------------
1Ô∏è‚É£ Public         ‚Üí full access everywhere  
2Ô∏è‚É£ Protected      ‚Üí limited access (class + subclass)  
3Ô∏è‚É£ Private        ‚Üí restricted access (inside class only)
 4. deafult       -> its a deafult access modifier(same package)

NOTE:
Python does NOT enforce strict access control.
It follows naming conventions instead.

===================================================================
# 1Ô∏è‚É£ PUBLIC MEMBERS (Default)
# ---------------------------------------------------------------
DEFINITION:
Public members can be accessed anywhere (inside class, outside class,
subclasses, objects). No underscore is used.

USE:
‚úî Use when the data/method should be openly accessible.
===================================================================
"""

class PublicDemo:
    def __init__(self):
        self.name = "Akash"   # public variable

    def display(self):        # public method
        print("Name =", self.name)

obj = PublicDemo()
obj.display()
print(obj.name)   # public ‚Üí accessible everywhere


print("------------------------------------------------------------")
"""
====================================================================
# 2Ô∏è‚É£ PROTECTED MEMBERS (_variable)
# ---------------------------------------------------------------
DEFINITION:
Protected members start with ONE underscore (_) and are accessible
inside the class and in subclasses.

#protected members = they are accessible within the same package and also available to all subclasses.
also have inheritance.

USE:
‚úî When you want to allow partial access
‚úî Prevent accidental use outside class
====================================================================
"""

class ProtectedDemo:
    def __init__(self):
        self._balance = 5000   # protected variable

    def _show_balance(self):   # protected method
        print("Balance =", self._balance)

class Bank(ProtectedDemo):
    def access_balance(self):
        print("Accessing protected member inside subclass:")
        self._show_balance()

b = Bank()
b.access_balance()

# Technically accessible but not recommended
print(b._balance)


print("------------------------------------------------------------")
"""
====================================================================
# 3Ô∏è‚É£ PRIVATE MEMBERS (__variable)
# ---------------------------------------------------------------
DEFINITION:
Private members start with TWO underscores (__) and can only be
accessed inside the class. Python applies *name mangling* internally:
__var ‚Üí _ClassName__var

USE:
‚úî Hide sensitive data completely  
‚úî Allow access only using getter/setter methods  
‚úî Prevent accidental modification  
====================================================================
"""

class PrivateDemo:
    def __init__(self):
        self.__pin = 1234       # private variable

    def __show_pin(self):       # private method
        print("PIN =", self.__pin)

    def access_private(self):
        print("Accessing private inside class:")
        self.__show_pin()

p = PrivateDemo()
p.access_private()

# ‚ùå Direct access = ERROR
# print(p.__pin)

# ‚úî Access using name-mangling (not recommended)
print(p._PrivateDemo__pin)


print("------------------------------------------------------------")
"""
====================================================================
SUMMARY
====================================================================
‚úî PUBLIC (name)  
    ‚Üí Accessible everywhere  
    ‚Üí Use for general data & functions

‚úî PROTECTED (_name)  
    ‚Üí Accessible in class + child class  
    ‚Üí Use for semi-secure data

‚úî PRIVATE (__name)  
    ‚Üí Accessible ONLY inside class  
    ‚Üí Use for highly sensitive data  
    ‚Üí Uses name-mangling ‚Üí _ClassName__var

‚úî Access Modifiers help in:
    ‚Ä¢ Encapsulation  
    ‚Ä¢ Security  
    ‚Ä¢ Code organization  
    ‚Ä¢ Preventing accidental misuse  

END OF NOTES.
"""
