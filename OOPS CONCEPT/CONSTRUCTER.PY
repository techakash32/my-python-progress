"""
======================================
üìå PYTHON CONSTRUCTOR (__init__) NOTES + EXAMPLES
======================================

A constructor is a special method used to **initialize an object**.

‚úî __init__ runs automatically when object is created  
‚úî Used to initialize object data  
‚úî self refers to the object's memory  
‚úî You can create parameterized constructors to store values  
‚úî Instance variables are created inside __init__ using self

‚úî Runs automatically when an object is created  
‚úî Method name is always:  __init__()  
‚úî Used to assign values to instance variables

__init__() ‚Üí  constructor


"""


# 1Ô∏è‚É£ DEFAULT CONSTRUCTOR (No parameters)
# ---------------------------------------

class housedesign:
    color = 'white'
    price = 500000


class housedesign:
    def __init__(self):   # Constructor
        print("This is constructor, builder ko bulaya ja rha h")


h1 = housedesign()   # Object created ‚Üí constructor runs automatically
print(h1)


"""
üìå self  
`self` is a reference variable that points to the CURRENT object.
Used to access instance variables.
"""


# 2Ô∏è‚É£ PARAMETERIZED CONSTRUCTOR
# -----------------------------

class housedesign:
    def __init__(self, color, price):   # parameterized constructor
        self.color = color     # instance variable
        self.price = price     # instance variable

print("*******************************")

h1 = housedesign("red", 600000)
print(h1.color)       # red
print(h1.price)       # 600000

print("*******************************")

h2 = housedesign("white", 1000000)
print(h2.color)       # white
print(h2.price)       # 1000000

print("*******************************")


