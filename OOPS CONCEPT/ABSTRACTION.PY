from abc import ABC, abstractmethod

class Bank(ABC):
    @abstractmethod
    def deposit(self):
        pass

    @abstractmethod
    def withdraw(self):
        pass

class SBI(Bank):
    def deposit(self):
        print("Depositing money in SBI...")

    def withdraw(self):
        print("Withdrawing money from SBI...")

b = SBI()
b.deposit()
b.withdraw()


print("---------------------------------------------------------")
