class Student:
    def __init__(self, name, roll):
        self.name = name
        self.roll = roll

    def show(self):
        print("Name:", self.name, "| Roll:", self.roll)

s1 = Student("Akash", 101)
s1.show()

print("------------------------------------------------------------")
"""
====================================================================
# 2Ô∏è‚É£ INHERITANCE
# ---------------------------------------------------------------
DEFINITION:
Inheritance allows one class (child) to acquire the properties and
methods of another class (parent).

USE:
‚úî Code reusability  
‚úî Better organization  
‚úî Extending existing functionalities  
====================================================================
"""

class Parent:
    def home(self):
        print("Parent has a house")

class Child(Parent):
    def car(self):
        print("Child has a car")

c = Child()
c.home()
c.car()

print("------------------------------------------------------------")
"""
====================================================================
# 3Ô∏è‚É£ POLYMORPHISM
# ---------------------------------------------------------------
DEFINITION:
Polymorphism means "one task, many forms."  
The same function name behaves differently depending on context.

TYPES:
‚úî Method Overriding  
‚úî Duck Typing  
‚úî Operator Overloading  
====================================================================
"""

class Bird:
    def sound(self):
        print("Bird makes sound")

class Sparrow(Bird):
    def sound(self):
        print("Sparrow chirps")

b = Sparrow()
b.sound()

print("------------------------------------------------------------")
"""
====================================================================
# 4Ô∏è‚É£ ENCAPSULATION
# ---------------------------------------------------------------
DEFINITION:
Encapsulation means combining data + methods inside a class and
restricting direct access using access modifiers.

USE:
‚úî Protect sensitive data  
‚úî Avoid accidental modification  
‚úî Maintain code security  
====================================================================
"""

class Bank:
    def __init__(self):
        self.__balance = 5000   # private

    def show_balance(self):
        print("Balance =", self.__balance)

b = Bank()
b.show_balance()

print("------------------------------------------------------------")
"""
====================================================================
# 5Ô∏è‚É£ ABSTRACTION
# ---------------------------------------------------------------
DEFINITION:
Abstraction means hiding complex details and showing only what is
necessary.

USE:
‚úî Reduce complexity  
‚úî Hide internal logic  
‚úî Provide clean & simple interfaces  
====================================================================
"""

from abc import ABC, abstractmethod

class Vehicle(ABC):
    @abstractmethod
    def start(self):
        pass

class Car(Vehicle):
    def start(self):
        print("Car starts with key")

c = Car()
c.start()

print("------------------------------------------------------------")
